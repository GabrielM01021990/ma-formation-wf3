/* ----------------------------- Incrémentation et décrémentation ----------------------------- */

// -------------------------------------- Incrémentation 

var nb1 = 1;
nb1 = nb1 + 1;
// -- Affichage dans la console
console.log(nb1);

// -- Ecriture simplifié
nb1++; 
//-- Raccourci de nb1+1
// -- Affichage dans la console
console.log(nb1);

// --------------------------------------- Incrémentation 

nb1 = nb1 - 1;
// -- Affichage dans la console
console.log(nb1);

// -- Ecriture simplifié
nb1--; 
//-- Raccourci de nb1-1
// -- Affichage dans la console
console.log(nb1);

// -- Subtilité
nb1 = 1;
console.log("nb1++ " + nb1++);

nb1 = 1;
console.log("++nb1 "+ ++nb1);




